// Modules - (Os)

// Normally we import modules using require statement
// Os Module(Operating System)

// importing os modules using require
const os = require('os');

// To know operating system of our os (windows) we use type method
console.log(os.type());  
// Os version
console.log(os.version());  
// free memory Space
console.log(os.freemem());
// CPU Status  
console.log(os.cpus());  




